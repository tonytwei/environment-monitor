		<h1 class="text-lg font-semibold">Pollutant Types:</h1>
		<p class="text-lg">The app tracks various types of pollutants including:</p>
		<p>
			Nitrogen Dioxide (NO2): Nitrogen dioxide is a highly reactive gas formed by emissions from
			motor vehicles, industry, unflued gas-heaters and gas stove tops. High concentrations can be
			found especially near busy roads and indoors where unflued gas-heaters are in use. Nitrogen
			dioxide is a respiratory irritant and has a variety of adverse health effects on the
			respiratory system.
		</p>
		<p>
			Sulfur Dioxide (SO2): Sulphur dioxide is highly reactive gas with a pungent irritating smell.
			It is formed by fossil fuel combustion at power plants and other industrial facilities.
			Sulphur dioxide irritates the lining of the nose, throat and lungs and may worsen existing
			respiratory illness especially asthma. It has also been found to exacerbate cardiovascular
			diseases.
		</p>
		<p>
			Carbon Monoxide (CO): Carbon monoxide is a poisonous gas that you can't see, taste or smell.
			It is produced from burning fuels like gas, wood and charcoal, even if there is no smoke.
			Carbon monoxide poisoning often occurs when people use outdoor devices indoors or in a closed
			space without enough air flow.
		</p>
		<p>
			Ozone (O3): Ozone at ground level is damaging to our health. Ground level ozone is the main
			component of smog and is the product of the interaction between sunlight and emissions from
			sources such as motor vehicles and industry. Ozone can travel long distances and accumulate to
			high concentrations far away from the sources of the original pollutants. Ground level ozone
			can be harmful to our health even at low levels.
		</p>
		<p>
			Particulate Matter (PM10): With a diameter of 10 micrometers of less, these particles are
			small enough to pass through the throat and nose and enter the lungs. Once inhaled, these
			particles can affect the heart and lungs and cause serious health effects.
		</p>
		<p>
			Particulate Matter (PM2.5): With a diameter of 2.5 micrometers or less, these particles are so
			small they can get deep into the lungs and into the bloodstream. There is sufficient evidence
			that exposure to PM2.5 over long periods (years) can cause adverse health effects.
		</p>
	</div>
